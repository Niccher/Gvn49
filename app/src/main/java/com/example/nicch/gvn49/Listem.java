public class Listem {
    private String AA;
    private String BB;
    private String CC;
    private String DD;
    private String EE;
    private String FF;

    public Listem(String AA, String BB, String CC, String DD, String EE, String FF) {
        this.AA = AA;
        this.BB = BB;
        this.CC = CC;
        this.DD = DD;
        this.EE = EE;
        this.FF = FF;
    }


    public String getAA() {
        return AA;
    }

    public String getBB() {
        return BB;
    }

    public String getCC() {
        return CC;
    }

    public String getDD() {
        return DD;
    }

    public String getEE() {
        return EE;
    }

    public String getFF() {
        return FF;
    }
}

